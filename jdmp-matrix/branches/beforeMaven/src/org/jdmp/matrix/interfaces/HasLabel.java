package org.jdmp.matrix.interfaces;

public interface HasLabel {

	/**
	 * Returns the String label of an object.
	 * 
	 * @return the label
	 */
	public String getLabel();

	/**
	 * Sets the label of an object
	 * 
	 * @param label
	 *            the new label.
	 */
	public void setLabel(String label);

}
